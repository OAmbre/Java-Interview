public class NamingRules {
    public static void main(String[] args) {
        // 变量命名只能使用字母 数字 $ _
        // 变量第一个字符 只能使用 字母 $ _
        // 变量第一个字符 不能使用数字
        // 注：_ 是下划线，不是-减号或者—— 破折号
        int a= 5;
        int a_12= 5;
        int $a43= 5;

        int a434= 5;

         //第一个是数字，是不行的
//        int 34a= 5;




        // 在命名的时候，尽量使用完整的单词进行命名，比如name,moveSpeed，而不是使用缩写 n,m。
        // 不能只使用关键字，但是可以包含关键字
        // 中文也是可以用来命名变量的
        // 但别这么干 哈哈哈
        




    }


}
